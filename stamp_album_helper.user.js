function insertButtons (target, stamp) {
    const hasSSW = document.querySelector('#sswmenu');
    if (hasSSW) {
        const sswButton = document.createElement('div');
        sswButton.style.width = '20px';
        sswButton.style.height = '20px';
        sswButton.style.cursor = 'pointer';
        sswButton.style.display = 'inline-block';
        sswButton.innerHTML = '<img src="https://images.neopets.com/shopkeepers/super_shopwizard.gif" style="width: 100%; height: 100%;" />';
        sswButton.addEventListener('click', function() {
            if (document.querySelector('.sswdrop.panel_hidden')) {
                document.querySelector('#sswmenu .imgmenu').click();
            }
            document.querySelector('#searchstr').value = stamp;
            document.querySelector('#ssw-criteria').selectedIndex = 1;
            document.querySelector('#price-limited').checked = false;
            document.querySelector('#button-search').click();
        });
        target.appendChild(sswButton);
    }
    const swButton = document.createElement('a');
    swButton.style.width = '20px';
    swButton.style.height = '20px';
    swButton.style.display = 'inline-block';
    swButton.innerHTML = '<img src="https://images.neopets.com/shopkeepers/shopwizard.gif" style="width: 100%; height: 100%;" />';
    swButton.href = `https://www.neopets.com/shops/wizard.phtml?string=${stamp}`;
    swButton.target = '_blank';
    target.appendChild(swButton);
    const tpButton = document.createElement('a');
    tpButton.style.width = '20px';
    tpButton.style.height = '20px';
    tpButton.style.display = 'inline-block';
    tpButton.innerHTML = '<img src="https://images.neopets.com/new_shopkeepers/29.gif" style="width: 100%; height: 100%;" />';
    tpButton.href = `http://www.neopets.com/island/tradingpost.phtml?type=browse&criteria=item_exact&search_string=${stamp}`;
    tpButton.target = '_blank';
    target.appendChild(tpButton);
    const auctionButton = document.createElement('a');
    auctionButton.style.width = '20px';
    auctionButton.style.height = '20px';
    auctionButton.style.display = 'inline-block';
    auctionButton.innerHTML = '<img src="https://images.neopets.com/shopkeepers/auctiongenie.gif" style="width: 100%; height: 100%;" />';
    auctionButton.href = `http://www.neopets.com/genie.phtml?type=process_genie&criteria=exact&auctiongenie=${stamp}`;
    auctionButton.target = '_blank';
    target.appendChild(auctionButton);
}

(function() {
    'use strict';

    // Your code here...
    const album = document.querySelector('[height="30"]').textContent.replace('- ', '').replace(' -', '');
    const stampsInAlbum = stamps.filter((stamp) => stamp.album === album);
    const cells = document.querySelectorAll('[height="450"] td[width="90"]');
    for (let i = 0; i < cells.length; i += 1) {
        const cell = cells[i];
        if (cell.querySelector('[alt="No Stamp"]')) {
            const stampAtThisPosition = stampsInAlbum.find((stamp) => stamp.index === i);
            if (stampAtThisPosition.name !== 'No Stamp') {
                const imgEl = cell.querySelector('[alt="No Stamp"]');
                imgEl.src = stampAtThisPosition.image;
                imgEl.style.opacity = '50%';
                insertButtons(cell, stampAtThisPosition.name);
            }
        }
    }
})();
